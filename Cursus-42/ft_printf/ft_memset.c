#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	int		i;
	char	*ts;

	ts = (char *)s;
	i = 0;
	while (i < (int)n)
	{
		ts[i] = c;
		i++;
	}
	return (s);
}
